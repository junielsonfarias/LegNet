// Service functions for Noticias
export const noticiasService = {
  getAll: () => noticiasData,
  
  getPublished: () => noticiasData.filter(noticia => noticia.publicada),
  
  getById: (id: number) => noticiasData.find(noticia => noticia.id === id),
  
  getByCategoria: (categoria: string) => 
    noticiasData.filter(noticia => noticia.categoria === categoria),
  
  getByTag: (tag: string) => 
    noticiasData.filter(noticia => 
      noticia.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
    ),
  
  getByAutor: (autor: string) => 
    noticiasData.filter(noticia => 
      noticia.autor.toLowerCase().includes(autor.toLowerCase())
    ),
  
  getRecent: (limit: number = 5) => 
    noticiasData
      .filter(noticia => noticia.publicada)
      .sort((a, b) => new Date(b.dataPublicacao).getTime() - new Date(a.dataPublicacao).getTime())
      .slice(0, limit),
  
  search: (term: string) => 
    noticiasData.filter(noticia => 
      noticia.titulo.toLowerCase().includes(term.toLowerCase()) ||
      noticia.resumo.toLowerCase().includes(term.toLowerCase()) ||
      noticia.conteudo.toLowerCase().includes(term.toLowerCase()) ||
      noticia.tags.some(tag => tag.toLowerCase().includes(term.toLowerCase()))
    ),
  
  getEstatisticas: () => ({
    total: noticiasData.length,
    publicadas: noticiasData.filter(noticia => noticia.publicada).length,
    rascunhos: noticiasData.filter(noticia => !noticia.publicada).length,
    categorias: Array.from(new Set(noticiasData.map(noticia => noticia.categoria))).length,
    totalVisualizacoes: noticiasData.reduce((total, noticia) => total + noticia.visualizacoes, 0)
  }),
  
  getCategorias: () => Array.from(new Set(noticiasData.map(noticia => noticia.categoria))),
  
  getTags: () => Array.from(new Set(noticiasData.flatMap(noticia => noticia.tags)))
}
